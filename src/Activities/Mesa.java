package Activities;

public class Mesa {

	    private String image;

	    public Mesa() {
	        super();
	    }
	    public Mesa(String urlImagen)
	    {	
	    	super();
	    	image = urlImagen;    	
	    }

	    public String getImage() {
	        return image;
	    }

	    public void setImage(String image) {
	        this.image = image;
	    }
	
}
